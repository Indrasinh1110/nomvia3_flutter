  static User _createUser(String id, String name, String uname, String bio) {
    // Assign random upcoming trips from _baseTrips
    final randomTripIndex = (id.hashCode) % _baseTrips.length;
    final randomTrip = _baseTrips[randomTripIndex];
    
    return User(
      id: id,
      name: name,
      username: uname,
      bio: bio,
      profileImageUrl: 'https://i.pravatar.cc/150?u=$id',
      travellerType: 'Backpacker',
      tripsDoneCount: (id.length * 2) % 15,
      friendsCount: (id.length * 10) % 200,
      upcomingTrips: [randomTrip],
    );
  }

